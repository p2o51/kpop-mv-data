#!/usr/bin/env node
/**
 * Emit SQL upserts for tracked_channels only.
 * Usage: node scripts/sync-channels-sql.mjs > /tmp/channels.sql
 */
import { loadRegistry } from "./lib/registry.mjs";

function sqlStr(v) {
  if (v == null) return "NULL";
  return `'${String(v).replace(/'/g, "''")}'`;
}

const { channels } = loadRegistry();

console.log("-- generated by scripts/sync-channels-sql.mjs");
for (const ch of channels) {
  console.log(
    `INSERT INTO tracked_channels (channel_id, registry_id, uploads_playlist_id, priority, active)
VALUES (${sqlStr(ch.youtube_channel_id)}, ${sqlStr(ch.id)}, ${sqlStr(ch.uploads_playlist_id)}, ${sqlStr(ch.priority ?? "normal")}, 1)
ON CONFLICT(channel_id) DO UPDATE SET
  registry_id = excluded.registry_id,
  uploads_playlist_id = excluded.uploads_playlist_id,
  priority = excluded.priority,
  active = 1;`,
  );
}
