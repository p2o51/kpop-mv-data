#!/usr/bin/env node
/**
 * Emit SQL upserts for act_groups + tracked_channels + tracked_videos from
 * registry YAML.
 * Usage:
 *   node scripts/sync-registry-sql.mjs > /tmp/kpop-sync.sql
 *   npx wrangler d1 execute kpop-mv --remote --file=/tmp/kpop-sync.sql
 */
import { loadRegistry } from "./lib/registry.mjs";

function sqlStr(v) {
  if (v == null) return "NULL";
  return `'${String(v).replace(/'/g, "''")}'`;
}

const { groups, channels, videos } = loadRegistry();
const channelByRegistry = new Map(channels.map((c) => [c.id, c]));

console.log("-- generated by scripts/sync-registry-sql.mjs");
// Only registry-managed rows are mass-deactivated; rows the worker discovered
// keep collecting until the registry adopts them (next discover + sync).
console.log("UPDATE tracked_videos SET active = 0 WHERE discovered_by = 'registry';");
console.log("UPDATE tracked_channels SET active = 0;");
console.log("DELETE FROM act_groups;");
// D1 wrangler execute rejects BEGIN/COMMIT — statements run in an implicit batch.

for (const g of groups) {
  if (g.gender && g.gender !== "female") continue;
  const aliases = g.aliases ?? [];
  console.log(
    `INSERT INTO act_groups (group_id, name, name_ko, aliases)
VALUES (${sqlStr(g.id)}, ${sqlStr(g.name)}, ${sqlStr(g.name_ko)}, ${sqlStr(JSON.stringify(aliases))});`,
  );
}

for (const ch of channels) {
  console.log(
    `INSERT INTO tracked_channels (channel_id, registry_id, uploads_playlist_id, priority, active, kind, track_groups)
VALUES (${sqlStr(ch.youtube_channel_id)}, ${sqlStr(ch.id)}, ${sqlStr(ch.uploads_playlist_id)}, ${sqlStr(ch.priority ?? "normal")}, 1, ${sqlStr(ch.kind ?? "artist")}, ${sqlStr(JSON.stringify(ch.track_groups ?? []))})
ON CONFLICT(channel_id) DO UPDATE SET
  registry_id = excluded.registry_id,
  uploads_playlist_id = excluded.uploads_playlist_id,
  priority = excluded.priority,
  active = 1,
  kind = excluded.kind,
  track_groups = excluded.track_groups;`,
  );
}

for (const v of videos) {
  const ch = channelByRegistry.get(v.channel);
  const channelId = ch?.youtube_channel_id ?? null;
  const active = v.active === true ? 1 : 0;
  console.log(
    `INSERT INTO tracked_videos (video_id, channel_id, group_id, video_class, published_at, active, priority_boost, title, duration_seconds, discovered_by)
VALUES (${sqlStr(v.youtube_video_id)}, ${sqlStr(channelId)}, ${sqlStr(v.group)}, ${sqlStr(v.video_class ?? "other")}, ${sqlStr(v.published_at)}, ${active}, 0, ${sqlStr(v.title)}, ${v.duration_seconds ?? "NULL"}, 'registry')
ON CONFLICT(video_id) DO UPDATE SET
  channel_id = excluded.channel_id,
  group_id = excluded.group_id,
  video_class = excluded.video_class,
  published_at = excluded.published_at,
  active = excluded.active,
  title = excluded.title,
  duration_seconds = excluded.duration_seconds,
  discovered_by = 'registry';`,
  );
}
